void main() {
  //join two maps, they're similar, now we have data through ages
  final allTimeMap = {
    ...mapBefore2010,
    ...mapAfter2010,
  };

  //The task required just machines statistic, create machines Set to avoid duplicates
  Set<AgriculturalMachinery> allMachineries = {};

  allTimeMap.forEach(
    (key, value) {
      for (var element in value) {
        allMachineries.addAll(element.machineries);
      }
    },
  );

  //Calculate list of ages, not just release dates
  List<int> ages = allMachineries
      .map((item) => DateTime.now().year - item.releaseDate.year)
      .toList();

  //Calculating average age all machines
  final averageAge = surf_dart_courses_template.getAverage(ages);

  print('Average age is $averageAge');

  //Creating 50% oldest machines list
  ages.sort((a, b) => b.compareTo(a));

  ages.length = (ages.length / 2).floor();

  //Calculating average age oldest machines
  final averageAgeOldestMachineries =
      surf_dart_courses_template.getAverage(ages);

  print('Average age oldest machineries is $averageAgeOldestMachineries');
}
